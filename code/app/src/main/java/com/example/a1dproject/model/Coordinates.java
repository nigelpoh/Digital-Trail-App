package com.example.a1dproject.model;

public class Coordinates {
    private double lat;
    private double lng;

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLng() {
        return lng;
    }

    public void setLong(double lng) {
        this.lng = lng;
    }
}